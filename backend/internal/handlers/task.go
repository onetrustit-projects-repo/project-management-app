package handlers

import (
	"net/http"

	"github.com/gin-gonic/gin"
	"github.com/google/uuid"
	"github.com/pm-app/backend/internal/models"
	"github.com/pm-app/backend/internal/services"
	ws "github.com/pm-app/backend/internal/websocket"
)

type TaskHandler struct {
	taskService *services.TaskService
	wsHub       *ws.Hub
}

func NewTaskHandler(ts *services.TaskService, hub *ws.Hub) *TaskHandler {
	return &TaskHandler{taskService: ts, wsHub: hub}
}

func (h *TaskHandler) ListByProject(c *gin.Context) {
	key := c.Param("key")
	tasks, err := h.taskService.ListByProject(c.Request.Context(), key)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}

	c.JSON(http.StatusOK, tasks)
}

func (h *TaskHandler) Create(c *gin.Context) {
	key := c.Param("key")
	var req models.CreateTaskRequest
	if err := c.ShouldBindJSON(&req); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}

	userID := c.MustGet("user_id").(uuid.UUID)
	task, err := h.taskService.Create(c.Request.Context(), key, userID, &req)
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}

	// Broadcast to project room
	h.wsHub.BroadcastToProject(key, models.WSMessage{
		Type: "task_created",
		Data: task,
	})

	c.JSON(http.StatusCreated, task)
}

func (h *TaskHandler) Get(c *gin.Context) {
	id, err := uuid.Parse(c.Param("id"))
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "invalid task id"})
		return
	}

	task, err := h.taskService.Get(c.Request.Context(), id)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}
	if task == nil {
		c.JSON(http.StatusNotFound, gin.H{"error": "task not found"})
		return
	}

	c.JSON(http.StatusOK, task)
}

